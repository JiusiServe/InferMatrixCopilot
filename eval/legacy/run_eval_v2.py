#!/usr/bin/env python3
"""RQS v2 rerun — literature-grounded metric over the cached v1 arm reviews.

New vs v1 (see METRIC_V2.md):
- CRScore-style pseudo-references: LLM claims about the diff (generated by a
  different model than the arms) + ruff findings filtered to added lines;
  comprehensiveness/conciseness from finding<->reference alignment.
- Severity- and resolution-weighted human-issue recall.
- Per-finding actionability.
- Judge JURY (majority/mean over models in V2_JUDGE_MODELS env, default
  deepseek-v4-pro + deepseek-v4-flash) with Cohen's kappa reported. All arms
  are the same generator model, so same-family judging biases levels, not the
  arm ranking; a cross-family judge can be plugged in via env when available.

Arm reviews/findings are reused from eval/raw/ (the reviews did not change);
all v2 judgments cache as raw/v2_*.json.
"""

from __future__ import annotations

import json
import os
import re
import subprocess
import sys
from pathlib import Path

EVAL_DIR = Path(__file__).resolve().parent
RAW = EVAL_DIR / "raw"
sys.path.insert(0, str(EVAL_DIR.parent / "src"))

from omni_copilot.config import Settings  # noqa: E402
from omni_copilot.llm import LLM, parse_json_reply  # noqa: E402

from eval.legacy.run_eval import ARMS, GROUND_TRUTH, PRS  # noqa: E402  (v1 harness)

# severity/resolution weights per METRIC_V2.md (blocking x2, nit x0.75, resolved x1.5)
GT_WEIGHTS: dict[int, dict[str, float]] = {}
_GT_META = {
    4678: {"gt1": ("normal", True), "gt2": ("normal", True)},
    4679: {"gt1": ("blocking", True), "gt2": ("nit", False),
           "gt3": ("nit", False), "gt4": ("blocking", True)},
    4849: {"gt1": ("normal", True), "gt2": ("normal", False)},
}
_SEV = {"blocking": 2.0, "normal": 1.0, "nit": 0.75}
for _pr, _meta in _GT_META.items():
    GT_WEIGHTS[_pr] = {gid: _SEV[sev] * (1.5 if resolved else 1.0)
                       for gid, (sev, resolved) in _meta.items()}

JUDGE_MODELS = [m.strip() for m in os.environ.get(
    "V2_JUDGE_MODELS", "deepseek-v4-pro,deepseek-v4-flash").split(",") if m.strip()]
CLAIMS_MODEL = os.environ.get("V2_CLAIMS_MODEL", "deepseek-v4-flash")
OMNI_REPO = Path(os.environ.get("OMNI_REPO", "/rebase/vllm-omni"))


def _cached(path: Path, fn):
    if path.exists():
        return json.loads(path.read_text())
    value = fn()
    path.write_text(json.dumps(value, indent=1, ensure_ascii=False))
    return value


# ── pseudo-references (CRScore-style, neuro-symbolic) ─────────────────────────

def _added_lines(diff: str) -> dict[str, set[int]]:
    """file -> set of new-file line numbers added by the diff."""
    out: dict[str, set[int]] = {}
    current, new_line = None, 0
    for line in diff.splitlines():
        if line.startswith("+++ b/"):
            current = line[6:]
            out.setdefault(current, set())
        elif line.startswith("@@"):
            m = re.search(r"\+(\d+)", line)
            new_line = int(m.group(1)) if m else 0
        elif current and line.startswith("+") and not line.startswith("+++"):
            out[current].add(new_line)
            new_line += 1
        elif current and not line.startswith("-"):
            new_line += 1
    return out


def static_findings(diff: str) -> list[str]:
    """ruff findings on changed .py files, filtered to lines this diff added."""
    added = _added_lines(diff)
    refs = []
    for path, lines in added.items():
        if not path.endswith(".py") or not lines:
            continue
        target = OMNI_REPO / path
        if not target.exists():
            continue
        try:
            out = subprocess.run(
                ["ruff", "check", "--output-format", "json", str(target)],
                capture_output=True, text=True, timeout=60)
            for item in json.loads(out.stdout or "[]"):
                row = (item.get("location") or {}).get("row")
                if row in lines:
                    refs.append(f"[static] {path}:{row} {item.get('code')}: "
                                f"{item.get('message')}")
        except Exception:
            continue
    return refs[:10]


def llm_claims(diff: str, llm: LLM) -> list[str]:
    reply = llm.create(
        system=("You analyze a PR diff and produce pseudo-references for review "
                "evaluation: factual claims about what the change does, its "
                "implications, risks, and anything a thorough reviewer could "
                "legitimately comment on (behavior changes, compat, tests, docs, "
                "naming, edge cases). 6-14 short, distinct, diff-grounded items. "
                'Output ONLY: {"claims": ["...", ...]}'),
        messages=[{"role": "user", "content": diff[:60_000]}],
        model=CLAIMS_MODEL, max_tokens=3000,
    )
    obj = parse_json_reply(reply.text) or {}
    return [str(c) for c in obj.get("claims", [])][:14]


def pseudo_refs(pr: int, diff: str, llm: LLM) -> list[str]:
    return _cached(RAW / f"v2_pr{pr}.refs.json",
                   lambda: llm_claims(diff, llm) + static_findings(diff))


# ── jury judgments ─────────────────────────────────────────────────────────────

def _findings_block(findings: list[dict]) -> str:
    return "\n".join(f"{i}. [{f.get('file', '')}:{f.get('line', '')}] "
                     f"{f.get('summary', '')}" for i, f in enumerate(findings))


def judge_validity(llm: LLM, model: str, findings, diff) -> list[bool]:
    if not findings:
        return []
    reply = llm.create(
        system=("Verify code-review findings against a PR diff. VALID = grounded "
                "in the diff/its clear context and technically plausible; INVALID "
                "= misreads the diff or factually wrong. Output ONLY: "
                '{"verdicts": [{"i": 0, "valid": true|false}]}'),
        messages=[{"role": "user", "content":
                   f"FINDINGS:\n{_findings_block(findings)}\n\n--- DIFF ---\n{diff[:60_000]}"}],
        model=model, max_tokens=3000)
    obj = parse_json_reply(reply.text) or {}
    v = {x.get("i"): bool(x.get("valid")) for x in obj.get("verdicts", [])}
    return [v.get(i, False) for i in range(len(findings))]


def judge_actionability(llm: LLM, model: str, findings) -> list[bool]:
    if not findings:
        return []
    reply = llm.create(
        system=("Judge ACTIONABILITY of code-review findings: actionable = the "
                "author can tell WHAT to change and WHERE (a location plus a "
                "concrete direction); not actionable = vague observation, praise, "
                "or a question with no direction. Output ONLY: "
                '{"verdicts": [{"i": 0, "actionable": true|false}]}'),
        messages=[{"role": "user", "content": _findings_block(findings)}],
        model=model, max_tokens=2000)
    obj = parse_json_reply(reply.text) or {}
    v = {x.get("i"): bool(x.get("actionable")) for x in obj.get("verdicts", [])}
    return [v.get(i, False) for i in range(len(findings))]


def judge_coverage(llm: LLM, model: str, findings, gt) -> dict[str, float]:
    numbered = "\n".join(f"- {f.get('summary', '')}" for f in findings) \
        or "(no findings)"
    gt_text = "\n".join(f"{g['id']}: {g['issue']}" for g in gt)
    reply = llm.create(
        system=("Compare review findings to ground-truth issues from human "
                "maintainers. Per issue: full (same problem), partial (right "
                "area, misses the core), miss. Output ONLY: "
                '{"coverage": [{"id": "gt1", "level": "full"|"partial"|"miss"}]}'),
        messages=[{"role": "user", "content":
                   f"GROUND TRUTH:\n{gt_text}\n\nFINDINGS:\n{numbered}"}],
        model=model, max_tokens=1500)
    obj = parse_json_reply(reply.text) or {}
    lv = {"full": 1.0, "partial": 0.5, "miss": 0.0}
    got = {c.get("id"): lv.get(c.get("level"), 0.0) for c in obj.get("coverage", [])}
    return {g["id"]: got.get(g["id"], 0.0) for g in gt}


def judge_alignment(llm: LLM, model: str, findings, refs) -> dict:
    """CRScore-style alignment via judge (no embedding endpoint available)."""
    refs_text = "\n".join(f"R{i}: {r}" for i, r in enumerate(refs))
    numbered = _findings_block(findings) or "(no findings)"
    reply = llm.create(
        system=("Align review findings with pseudo-references (claims/issues a "
                "review of this diff could address). covered_refs = indices of "
                "references substantively addressed by ANY finding; "
                "aligned_findings = indices of findings that match at least one "
                "reference. Output ONLY: "
                '{"covered_refs": [0,...], "aligned_findings": [0,...]}'),
        messages=[{"role": "user", "content":
                   f"REFERENCES:\n{refs_text}\n\nFINDINGS:\n{numbered}"}],
        model=model, max_tokens=1500)
    obj = parse_json_reply(reply.text) or {}
    return {"covered_refs": [i for i in obj.get("covered_refs", [])
                             if isinstance(i, int) and 0 <= i < len(refs)],
            "aligned_findings": [i for i in obj.get("aligned_findings", [])
                                 if isinstance(i, int) and 0 <= i < len(findings)]}


def jury(pr: int, arm: str, kind: str, fn) -> list:
    """Run one judgment across all jury models, cached per model."""
    out = []
    for model in JUDGE_MODELS:
        tag = model.replace("/", "_")
        out.append(_cached(RAW / f"v2_pr{pr}_{arm}.{kind}.{tag}.json",
                           lambda m=model: fn(m)))
    return out


# ── agreement (Cohen's kappa, 2 raters, binary) ────────────────────────────────

def cohens_kappa(a: list[bool], b: list[bool]) -> float | None:
    if len(a) != len(b) or not a:
        return None
    po = sum(1 for x, y in zip(a, b) if x == y) / len(a)
    pa1, pb1 = sum(a) / len(a), sum(b) / len(b)
    pe = pa1 * pb1 + (1 - pa1) * (1 - pb1)
    return 1.0 if pe == 1 else (po - pe) / (1 - pe)


# ── scoring ────────────────────────────────────────────────────────────────────

def hmean(values: list[float]) -> float:
    if any(v <= 0 for v in values):
        return 0.0
    return len(values) / sum(1 / v for v in values)


def main() -> int:
    settings = Settings()
    llm = LLM(settings)
    assert llm.available, "no API key"
    print(f"jury: {JUDGE_MODELS}; claims: {CLAIMS_MODEL}", flush=True)

    rows: dict[int, dict[str, dict]] = {}
    kappa_pool: dict[str, list[tuple[list, list]]] = {"validity": [], "action": [],
                                                      "align": []}
    for pr in PRS:
        diff = (RAW / f"pr{pr}.diff").read_text()
        refs = pseudo_refs(pr, diff, llm)
        print(f"PR {pr}: {len(refs)} pseudo-references", flush=True)
        gt = GROUND_TRUTH[pr]
        weights = GT_WEIGHTS[pr]
        rows[pr] = {}
        for arm in ARMS:
            findings = json.loads((RAW / f"pr{pr}_{arm}.findings.json").read_text())
            print(f"[judge] PR {pr} · {arm} ({len(findings)} findings)", flush=True)

            val_votes = jury(pr, arm, "validity",
                             lambda m: judge_validity(llm, m, findings, diff))
            act_votes = jury(pr, arm, "action",
                             lambda m: judge_actionability(llm, m, findings))
            cov_votes = jury(pr, arm, "coverage",
                             lambda m: judge_coverage(llm, m, findings, gt))
            aln_votes = jury(pr, arm, "align",
                             lambda m: judge_alignment(llm, m, findings, refs))

            n = len(findings)
            precision = (sum(sum(v) for v in val_votes)
                         / (n * len(val_votes))) if n else 0.0
            actionability = (sum(sum(v) for v in act_votes)
                             / (n * len(act_votes))) if n else 0.0
            cov = {g["id"]: sum(cv[g["id"]] for cv in cov_votes) / len(cov_votes)
                   for g in gt}
            recall_w = (sum(weights[g] * c for g, c in cov.items())
                        / sum(weights.values()))
            covered = [set(v["covered_refs"]) for v in aln_votes]
            aligned = [set(v["aligned_findings"]) for v in aln_votes]
            comp = (sum(len(c) for c in covered)
                    / (len(refs) * len(covered))) if refs else 0.0
            conc = (sum(len(a) for a in aligned)
                    / (n * len(aligned))) if n else 0.0

            if len(JUDGE_MODELS) >= 2 and n:
                kappa_pool["validity"].append((val_votes[0], val_votes[1]))
                kappa_pool["action"].append((act_votes[0], act_votes[1]))
                kappa_pool["align"].append((
                    [i in aligned[0] for i in range(n)],
                    [i in aligned[1] for i in range(n)]))

            cost = json.loads((RAW / f"pr{pr}_{arm}.cost.json").read_text())
            rows[pr][arm] = {
                "findings": n, "recall_w": recall_w, "precision": precision,
                "actionability": actionability, "comprehensiveness": comp,
                "conciseness": conc,
                "rqs": hmean([recall_w, precision, actionability]),
                "tokens": cost["input_tokens"] + cost["output_tokens"],
                "coverage": cov,
            }

    kappas = {}
    for kind, pairs in kappa_pool.items():
        a = [x for p in pairs for x in p[0]]
        b = [x for p in pairs for x in p[1]]
        k = cohens_kappa(a, b)
        kappas[kind] = k if k is not None else float("nan")

    write_results(rows, kappas)
    print(f"done -> {EVAL_DIR / 'RESULTS_V2.md'}")
    return 0


def write_results(rows: dict, kappas: dict) -> None:
    cols = ("findings", "recall_w", "precision", "actionability",
            "comprehensiveness", "conciseness", "rqs", "tokens")
    lines = ["# RQS v2 results", "",
             f"Metric: METRIC_V2.md. Jury: {JUDGE_MODELS} (same family — all "
             "arms share one generator model, so judge-family bias shifts "
             "levels, not the arm ranking). Claims model: " + CLAIMS_MODEL + ".",
             "", "Inter-judge agreement (Cohen's kappa, pooled): "
             + ", ".join(f"{k}={v:.2f}" for k, v in kappas.items()), ""]
    agg = {arm: {c: 0.0 for c in cols} for arm in ARMS}
    for pr in PRS:
        lines += [f"## PR #{pr}", "",
                  "| arm | findings | recall_w | precision | actionability | "
                  "comprehensiveness | conciseness | **RQS** | tokens |",
                  "|---|---|---|---|---|---|---|---|---|"]
        for arm in ARMS:
            r = rows[pr][arm]
            for c in cols:
                agg[arm][c] += r[c] / len(PRS)
            lines.append(
                f"| {arm} | {r['findings']} | {r['recall_w']:.2f} | "
                f"{r['precision']:.2f} | {r['actionability']:.2f} | "
                f"{r['comprehensiveness']:.2f} | {r['conciseness']:.2f} | "
                f"**{r['rqs']:.2f}** | {r['tokens']:,} |")
        lines += ["", "Weighted coverage: " + "; ".join(
            f"{arm}: " + ",".join(f"{k}={v:g}" for k, v in
                                  rows[pr][arm]["coverage"].items())
            for arm in ARMS), ""]
    lines += ["## Aggregate (mean over PRs)", "",
              "| arm | recall_w | precision | actionability | comprehensiveness "
              "| conciseness | **RQS** | tokens |",
              "|---|---|---|---|---|---|---|---|"]
    for arm in ARMS:
        a = agg[arm]
        lines.append(f"| {arm} | {a['recall_w']:.2f} | {a['precision']:.2f} | "
                     f"{a['actionability']:.2f} | {a['comprehensiveness']:.2f} | "
                     f"{a['conciseness']:.2f} | **{a['rqs']:.2f}** | "
                     f"{a['tokens']:,.0f} |")
    (EVAL_DIR / "RESULTS_V2.md").write_text("\n".join(lines) + "\n")


if __name__ == "__main__":
    sys.exit(main())
